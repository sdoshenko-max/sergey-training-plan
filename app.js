  function findTechniqueProfile(name) {
    const lowerName = name.toLowerCase();
    return techniqueProfiles.find(({ pattern }) => pattern.test(lowerName)) ?? defaultTechniqueProfile;
  }

  function buildLoadGuidance(name, sets) {
    const lowerName = name.toLowerCase();

    if (lowerName.includes("легко") || lowerName.includes("высокой опоры")) {
      return "Это восстановительный вариант: оставляй 3-4 повтора в запасе, темп держи спокойным и не превращай подход в добивание.";
    }

    if (/сек|м /.test(sets)) {
      return "Выбирай длительность или вес так, чтобы удержать ровную технику весь отрезок. Заканчивай подход до момента, когда корпус начинает компенсировать.";
    }

    if (/12|15|20/.test(sets)) {
      return "Вес должен давать чистый памп без потери траектории: оставляй 1-2 повтора в запасе, а прибавляй вес только когда все повторы идут одинаково.";
    }

    if (lowerName.includes("подтягивания")) {
      return "Подбери помощь или вариант так, чтобы каждый повтор был чистым. Лучше оставить 1-2 повтора в запасе, чем добивать подход рывками.";
    }

    return "Рабочий вес выбирай так, чтобы оставался запас 1-2 повтора. Если верхняя граница повторов выполнена во всех подходах чисто, на следующей тренировке можно немного добавить вес.";
  }

  function buildDetailedTechnique(name, sets, equipment, cue) {
    const profile = findTechniqueProfile(name);

    return [
      ["Настройка", profile.setup],
      ["Движение", `${cue} ${profile.movement}`],
      ["Дыхание", profile.breathing],
      ["Ошибки", profile.errors],
      ["Вес и запас", buildLoadGuidance(name, sets, equipment)],
    ];
  }

  function normalizeTechnique(name, sets, equipment, technique) {
    if (Array.isArray(technique)) {
      return technique;
    }

    return buildDetailedTechnique(name, sets, equipment, technique);
  }

  function exercise(name, sets, equipment, technique, options = {}) {
    return {
      name,
      sets,
      equipment,
      technique: normalizeTechnique(name, sets, equipment, technique),
      ...options,
    };
  }

